# Lesson 05: Control Flow and Loops

# If-Else Example
age = 18
if age >= 18:
    print("You are an adult.")
else:
    print("You are a minor.")


# For Loop Example
for i in range(5):
    print(f"Iteration {i}")


# While Loop Example
counter = 0
while counter < 3:
    print(f"Counter is at {counter}")
    counter += 1


# Nested Loop Example
for i in range(3):
    for j in range(2):
        print(f"i: {i}, j: {j}")
