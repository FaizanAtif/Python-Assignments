# Bitwise Operators
a, b = 5, 3  # 5 = 101, 3 = 011

print("a & b (AND):", a & b)   # 1  (001)
print("a | b (OR):", a | b)    # 7  (111)
print("a ^ b (XOR):", a ^ b)   # 6  (110)
print("~a (NOT):", ~a)         # -6
print("a << 1 (Left Shift):", a << 1)  # 10 (1010)
print("b >> 1 (Right Shift):", b >> 1) # 1  (001)
